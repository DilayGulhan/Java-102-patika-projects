package Containers;

public class BoxUncountables {
	private int i = 1;
	private String boxCode;
	private int mass;
	private int volume;
	private String serialNumber;
	

	
	public BoxUncountables(int i, String boxCode, int mass, int volume, String serialNumber) {
		this.i = i;
		this.boxCode = boxCode;
		this.mass = mass;
		this.volume = volume;
		this.serialNumber = serialNumber;
	}
	
}
